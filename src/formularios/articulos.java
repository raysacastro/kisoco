    private void cargar_clientes(){
        try{
            String query = "SELECT * FROM articulo WHERE (idart LIKE '%" + txtBuscar.getText() + "%' OR desart LIKE '%"+txtBuscar.getText()+"%') and estado = 1";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(query);
            DefaultTableModel model_table = (DefaultTableModel)tabla_productos.getModel();
            model_table.getDataVector().clear();
            String registros[] = new String[5];
            while(rs.next()){
                registros[0] = rs.getString("idart");
                registros[1] = rs.getString("desart");
                registros[2] = rs.getString("preven");
                model_table.addRow(registros);
            }  
        } catch(Exception error){
            JOptionPane.showMessageDialog(null, "Error: " + error);
        }
    }
