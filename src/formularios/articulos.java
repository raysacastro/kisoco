    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if(txtDescripcion.getText().equals("") || txtIdTipo.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe rellenar todos los campos");
        } else {
            String query = "UPDATE articulo SET desart = '" + txtDescripcion.getText() +"',preven = '" + txtDescripcion1.getText() +"' WHERE idart = '" + txtIdTipo.getText() + "'";
            try{
                PreparedStatement psz = cn.prepareStatement(query);
                psz.executeUpdate();
                JOptionPane.showMessageDialog(null, "Articulo Modificado");
                limpiar_campos();
                cargar_clientes();
            } catch(Exception error){
                JOptionPane.showMessageDialog(null, "Error: " + error);
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void tabla_productosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_productosMouseClicked
        DefaultTableModel table_model = (DefaultTableModel)tabla_productos.getModel();
        txtIdTipo.setText((String) table_model.getValueAt(tabla_productos.getSelectedRow(), 0));
        txtDescripcion.setText((String) table_model.getValueAt(tabla_productos.getSelectedRow(), 1));
        txtDescripcion1.setText((String) table_model.getValueAt(tabla_productos.getSelectedRow(), 2));
    }//GEN-LAST:event_tabla_productosMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(txtIdTipo.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe rellenar todos los campos");
        } else {
            String query = "UPDATE articulo SET estado = 0 WHERE idart = '" + txtIdTipo.getText() + "'";
            try{
                PreparedStatement psz = cn.prepareStatement(query);
                psz.executeUpdate();
                JOptionPane.showMessageDialog(null, "Articulo eliminado");
                limpiar_campos();
                cargar_clientes();
            } catch(Exception error){
                JOptionPane.showMessageDialog(null, "Error: " + error);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

